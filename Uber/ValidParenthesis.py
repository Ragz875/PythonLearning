def validate_expr(s):
    stack = []
    open_braces='({['
    closed_braces = ')}]'
    dic={'(':')','{':'}','[':']'}
    #print(s)
    for char in s:
        if char in open_braces:
            stack.append(char)
        elif char in closed_braces:
            if len(stack)==0:
                return False
            item=stack.pop()
            if dic[item]!=char:
                return False
    return len(stack)==0

