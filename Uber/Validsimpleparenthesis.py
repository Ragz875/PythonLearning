# This program is to print how many missing parenthesis in order to make it proper Parenthesis
# expression

def MissingCount(s):
    stack=[]
    misisng_cnt=0
    for char in s:
        if char == '(':
            stack.append(char)
        elif char == ')':
            if len(stack)!=0 :
                stack.pop()
            else:
                misisng_cnt += 1

    return len(stack)+misisng_cnt


